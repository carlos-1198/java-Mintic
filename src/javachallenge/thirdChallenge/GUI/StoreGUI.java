    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int actualRow = jTable1.getSelectedRow();
        if(actualRow != -1){
            this.productStorage.deleteFromGUI(this.jTable1.getModel().getValueAt(actualRow, 0));
            this.updateTable();
            JOptionPane.showMessageDialog(null, "El producto fue borrado exitosamente");
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un producto de la tabla", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtName.getText().isEmpty() || txtPrice.getText().isEmpty() || txtStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            if(this.productStorage.addFromGUI(txtName.getText(), txtPrice.getText(), txtStock.getText())){
                JOptionPane.showMessageDialog(null, "El producto fue agregado exitosamente");
                this.updateTable();      
            }else{ 
                JOptionPane.showMessageDialog(null, "El producto ya existe", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

