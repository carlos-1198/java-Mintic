    private void updateTable(){
        String data [][] = this.obtainDataTable();
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            data,
            new String [] {
                "ID", "Nombre", "Precio", "Inventario"
            }
        ));
    }
    
    private String[][] obtainDataTable(){
        HashMap<Integer, Product> productList = this.productStorage.getDatabase();
        String data [][] = new String[productList.values().size()][4];
        int i = 0;
        for (Product value : this.productStorage.getDatabase().values()) {
            data[i][0] = String.valueOf(value.getCode());
            data[i][1] = value.getName();
            data[i][2] = String.valueOf(value.getPrice());
            data[i][3] = String.valueOf(value.getQuantity());
            i++;
        }
        return data;
    }
